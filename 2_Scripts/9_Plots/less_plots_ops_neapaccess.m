Device_LineTypes = {'g-','g-.',...
                    'b-','b--',...
                    'r-','r--',...
                    'm-','m--'};  

for d = [2,4,6]
    plot([OPERATIONS.DISCRETE.SITES.DEVICES(d).OPERATIONS(1).OperationLimitedTimeOnSite.NeapAccess.NumberDaysRequiredOnSite],...
        Device_LineTypes{d},'linewidth',2); hold on;
    
        plot([OPERATIONS.DISCRETE.SITES.DEVICES(d).OPERATIONS(1).OperationLimitedTimeOnSite.NeapAccess.NumberStandbyDaysBetweenAccessPeriods],...
        Device_LineTypes{d},'linewidth',6); hold on; 

end

legend('PLATO_OnSite','PLATO_Standby',...
       'PLATI_OnSite','PLATI_Standby',...
       'HBMT_OnSite','HBMT_Standby',...
       'location','best','Interpreter', 'none');
   
